    private void btnrecibirActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnrecibirActionPerformed
        // BOTON RECIBIR
        String idpr,fec1,fec2,dat3,idl,codu,st;
        idpr=lblidp.getText();
        fec1=lblfec1.getText();
        dat3=fec3.getText();
        fec2=lblfec2.getText();
        st="C";
        idl=lblidl.getText();
        codu=lblcod.getText();
     
        String sql="CALL up_pr(?,?,?,?,?,?,?);";
        try {
                PreparedStatement env = cn.prepareStatement(sql);//CREAMOS EL METODO QUE EJECUTARA AL PROCEDIMIENTO ALMACENADO
                env.setString(1, idpr);
                env.setString(2, fec1);
                env.setString(3, dat3);
                env.setString(4, fec2);
                env.setString(5, st);
                env.setString(6, idl);
                env.setString(7, codu);
                env.executeUpdate();
                limpiar();
                tbpr("");
                JOptionPane.showMessageDialog(null, "Elemento actualizado");
                } catch (SQLException ex) {
            Logger.getLogger(aPrestamos.class.getName()).log(Level.SEVERE, null, ex);
        }
    }//GEN-LAST:event_btnrecibirActionPerformed

