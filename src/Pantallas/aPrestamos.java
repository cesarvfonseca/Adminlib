    void tbpr(String valor){
                String []titulos={"Cod_Prestamo","Id","Usuario","Cod_lib","Titulo","fecha prestamo","fecha compromiso","Fecha devolucion","Status","Sistema"};
                String []Registros= new String[10];
                model = new DefaultTableModel(null,titulos);
                String sql="";
                if (valor.equals("")){
                sql = "CALL pr_user();";
                  }else{
                sql="CALL con_prs2('%"+valor+"%');";
                }
            try{
                Statement st = cn.createStatement();
                ResultSet rs = st.executeQuery(sql);
                          while(rs.next())
                          {
                              Registros[0]= rs.getString("id_p");
                              Registros[1]= rs.getString("cod_usuario");
                              Registros[2]= rs.getString("nom");
                              Registros[3]= rs.getString("cod_libro");
                              Registros[4]= rs.getString("titulo");
                              Registros[5]= rs.getString("fecha_salida");
                              Registros[6]= rs.getString("fecha_maxima");
                              Registros[7]= rs.getString("fecha_devolucion");
                              Registros[8]= rs.getString("Status");
                              Registros[9]= rs.getString("status_sistema");
                              model.addRow(Registros);
                          }
                            tbpr.setModel(model);
                            } catch (SQLException ex) {
                            Logger.getLogger(aPrestamos.class.getName()).log(Level.SEVERE, null, ex);
                    }
    }

