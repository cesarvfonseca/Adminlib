    private void btnguardarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnguardarActionPerformed
        // Boton Guardar 
        String d,m,a;//SE DECLARAN LAS VARIABLES DE DIA, MES Y A√ëO PARA LA FECHA
                d=Integer.toString(jDate.getCalendar().get(Calendar.DAY_OF_MONTH));//OBTENEMOS EL DIA DEL JDATE
                m=Integer.toString(jDate.getCalendar().get(Calendar.MONTH));//OBTENEMOS EL MES DEL JDATE
                a=Integer.toString(jDate.getCalendar().get(Calendar.YEAR));//OBTENEMOS EL A√ëO DEL JDATE
        String nom,ape,sex,dom,cd,tel,mail,cp,fec_nac,tipo,pwd;//SE DECLARAN LAS VARIABLES QUE RECINIRAN LOS DATOS DEL FORMULARIO
                nom=txtnom.getText();
                ape=txtape.getText();
                sex=comsex.getSelectedItem().toString();
                dom=txtdom.getText();
                cd=txtcd.getText();
                tel=txttel.getText();
                mail=txtmail.getText();
                cp=txtcp.getText();
                fec_nac=(a+"-"+m+"-"+d);
                tipo=comtipo.getSelectedItem().toString();
                pwd=jpwd.getText();
            
            String sql="call biblioteca_db.altacli(?,?,?,?,?,?,?,?,?,?,?);";//LLAMANDO PROCEDIMIENTO ALMACENADO
       if(nom.isEmpty()||ape.isEmpty()||dom.isEmpty()||cd.isEmpty()||tel.isEmpty()||mail.isEmpty()||
            cp.isEmpty()||fec_nac.isEmpty()||pwd.isEmpty()){
            JOptionPane.showMessageDialog(null, "Falta campos por llenar, favor de verificar");
        }else{
        try {
                PreparedStatement env = cn.prepareStatement(sql);//CREAMOS EL METODO QUE EJECUTARA AL PROCEDIMIENTO ALMACENADO
                env.setString(1, nom);
                env.setString(2, ape);
                env.setString(3, sex);
                env.setString(4, dom);
                env.setString(5, cd);
                env.setString(6, tel);
                env.setString(7, mail);
                env.setString(8, cp);
                env.setString(9, fec_nac);
                env.setString(10, tipo);
                env.setString(11, pwd);
                env.executeUpdate();
                JOptionPane.showMessageDialog(null, "Registro Guardado con Exito");
                limpiar();
                bloquear();
                mostrard("");
        } catch (SQLException ex) {
            Logger.getLogger(Clientes.class.getName()).log(Level.SEVERE, null, ex);
        }
        }
    }//GEN-LAST:event_btnguardarActionPerformed

