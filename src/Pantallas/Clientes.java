    void mostrard (String valor){
          String []titulos={"Codigo","Nombres","Apellidos","Sexo","Domicilio","Ciudad","Telefono","Correo","CP","Fecha de Nacimiento","Tipo","Pass"};    
          String []Registros=new String[12];
          model= new DefaultTableModel(null,titulos);
          String sql="";
          if (valor.equals("")){
              sql="CALL con_clientes;";
          }else{
              sql="CALL con_clientes2('%"+valor+"%');";
          }
            try {
              Statement st = cn.createStatement();
              ResultSet rs = st.executeQuery(sql);
              while(rs.next())
              {
                  Registros[0]= rs.getString("cod_usuario");
                  Registros[1]= rs.getString("nombre");
                  Registros[2]= rs.getString("apellido");
                  Registros[3]= rs.getString("sexo");
                  Registros[4]= rs.getString("domicilio");
                  Registros[5]= rs.getString("ciudad");
                  Registros[6]= rs.getString("telefono");
                  Registros[7]= rs.getString("correo");
                  Registros[8]= rs.getString("cp");
                  Registros[9]= rs.getString("fecha_nacimiento");
                  Registros[10]= rs.getString("tipo");
                  Registros[11]= rs.getString("contra");
                  model.addRow(Registros);
              }
              tbclientes.setModel(model);
               
                } catch (SQLException ex) {
            Logger.getLogger(Clientes.class.getName()).log(Level.SEVERE, null, ex);
                }   
    }

