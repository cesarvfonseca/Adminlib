    private void jmodificarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jmodificarActionPerformed
        // TODO add your handling code here:
        btnmodificar.setEnabled(true);
        desbloquear();
        btnguardar.setEnabled(false);
        btnnuevo.setEnabled(false);
        int fila = tbclientes.getSelectedRow();
        if (fila>=0){
            lbid.setText(tbclientes.getValueAt(fila,0).toString());
            txtnom.setText(tbclientes.getValueAt(fila,1).toString());
            txtape.setText(tbclientes.getValueAt(fila,2).toString());
            comsex.setSelectedItem(tbclientes.getValueAt(fila, 3).toString());
            txtdom.setText(tbclientes.getValueAt(fila,4).toString());
            txtcd.setText(tbclientes.getValueAt(fila,5).toString());
            txttel.setText(tbclientes.getValueAt(fila,6).toString());
            txtmail.setText(tbclientes.getValueAt(fila,7).toString());
            txtcp.setText(tbclientes.getValueAt(fila,8).toString());
            jDate.setDate(Date.valueOf(tbclientes.getValueAt(fila,9).toString()));
            comtipo.setSelectedItem(tbclientes.getValueAt(fila, 10).toString());
            jpwd.setText(tbclientes.getValueAt(fila,11).toString());
        }else{
                JOptionPane.showMessageDialog(null,"No selecciono fila");   
        }
    }//GEN-LAST:event_jmodificarActionPerformed

