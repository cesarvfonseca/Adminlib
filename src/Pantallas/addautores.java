    private void popeditarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_popeditarActionPerformed
        // POP-MODIFICAR
        int fila = tbautores.getSelectedRow();
        String cod="",nom="",ape="",pais="";
        
        if (fila>=0){
            cod=(tbautores.getValueAt(fila,0).toString());
            nom=(tbautores.getValueAt(fila,1).toString());
            ape=(tbautores.getValueAt(fila,2).toString());
            pais=(tbautores.getValueAt(fila,3).toString());
        }
        lblcod.setText(cod);
        txtnom.setText(nom);
        txtape.setText(ape);
        txtpais.setText(pais);
        btnmodificar.setEnabled(true);
        desbloquear();
    }//GEN-LAST:event_popeditarActionPerformed

    private void btnmodificarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnmodificarActionPerformed
       // BOTON MODIFICAR
        String nom,ape,pais,cod;
        nom=txtnom.getText();
        ape=txtape.getText();
        pais=txtpais.getText();
        cod=lblcod.getText();
    try{
        String sql="CALL biblioteca_db.actautores(?,?,?,?);";
        PreparedStatement pst = cn.prepareStatement(sql);
        pst.setString(1, nom);
        pst.setString(2, ape);
        pst.setString(3, pais); 
        pst.setString(4, cod);
        pst.executeUpdate();
        JOptionPane.showMessageDialog(rootPane,"Actualizado");
        bloquear();
        tbautores("");
        limpiar();
    } catch(SQLException ex) {
        Logger.getLogger(Libros.class.getName()).log(Level.SEVERE, null, ex);
    }
    }//GEN-LAST:event_btnmodificarActionPerformed

