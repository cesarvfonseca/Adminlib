    private void jMenuItem3ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jMenuItem3ActionPerformed
        String mail = connuser.getText();
        String cod="",nom="",ape="";
        String sql = "SELECT * FROM biblioteca_db.usuarios WHERE correo='"+mail+"';";
        try {
            Statement st = cn.createStatement();
            ResultSet rs = st.executeQuery(sql);
            while(rs.next()){
                cod=rs.getString("cod_usuario");
                nom=rs.getString("nombre");
                ape=rs.getString("apellido");
            }
        ulibros lib = new ulibros();
        uescritorio.add(lib);
        lib.show();
        ulibros.lblid.setText(cod);
        ulibros.usrlbl.setText(nom+" "+ape);
        } catch (SQLException ex) {
            Logger.getLogger(pantalla_ingreso.class.getName()).log(Level.SEVERE, null, ex);
        }
    }//GEN-LAST:event_jMenuItem3ActionPerformed

    private void mprActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_mprActionPerformed
        // Mostrar tabla prestamos
        String mail = connuser.getText();
        String cod="",nom="",ape="",sexo="",dom="",cd="",tel="",correo="",cp="",f_n="";
        String sql = "SELECT * FROM biblioteca_db.usuarios WHERE correo='"+mail+"';";
        try {
            Statement st = cn.createStatement();
            ResultSet rs = st.executeQuery(sql);
            while(rs.next()){
                cod=rs.getString("cod_usuario");
                nom=rs.getString("nombre");
                ape=rs.getString("apellido");
                sexo=rs.getString("sexo");
                dom=rs.getString("domicilio");
                cd=rs.getString("ciudad");
                tel=rs.getString("telefono");
                correo=rs.getString("correo");
                cp=rs.getString("cp");
                f_n=rs.getString("fecha_nacimiento");
            }
        uprestamos upr = new uprestamos();
        uescritorio.add(upr);
        upr.show();
        uprestamos.lblid.setText(cod);
        uprestamos.btnid.setText(cod);
        uprestamos.lblnom.setText("Nombre(s): "+nom+" "+ape);
        uprestamos.lbltel.setText("Telefono: "+tel);
        uprestamos.lblcorreo.setText("Correo: "+correo);
        uprestamos.lbldom.setText("Domicilio: "+dom);
        uprestamos.lblcd.setText("Ciudad: "+cd);
        uprestamos.lblcp.setText("CP: "+cp);
        uprestamos.lbldate.setText("Fecha de Nacimiento: "+f_n);
        } catch (SQLException ex) {
            Logger.getLogger(pantalla_ingreso.class.getName()).log(Level.SEVERE, null, ex);
        }
    }//GEN-LAST:event_mprActionPerformed

